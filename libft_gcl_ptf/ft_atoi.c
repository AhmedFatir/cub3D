#include "libft.h"

int	ft_atoi(const char *str)
{
	int							i;
	int							s;
	unsigned long long			res;

	i = 0;
	s = 1;
	res = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			s = s * -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + str[i] - 48;
		if (res > 9223372036854775807 && s == 1)
			return (-1);
		else if (res > 9223372036854775807 && s == -1)
			return (0);
		i++;
	}
	return (res * s);
}
